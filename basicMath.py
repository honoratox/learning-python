# em Python não precisa informar o tipo de variável, se é float, int, double etc
# as expressões são separadas por vírgula


#adição
class adicao:
    print("Adição: ", 5+3)

#subtração
class subtracao:
    print("Subtração: ", 5-3)

#multiplicação
class multiplicacao:
    print("Multiplicação: ", 5*3)

#divisão (float)
class divisao: 
    print("Divisão float: ", 36/4)
    print("Divisão float: ",5/3)

#divisão (int)
class divisao_int:
    print("Divisão int: ", 7//3) #O // é a divisão inteira, ou seja, o resultado é um número inteiro

#Potenciação
class potenciacao:
    print("Potência: ", 5**3) #O '**' é o simbolo de potenciação, ou seja 5 elevado a 3
    #5*5 = 5^2 = 5**2 = 25
    print("Potência: ", 5**2)