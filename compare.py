#igualdade
print(10 == 10) #O '==' é o simbolo de igualdade, ou seja, 10 é igual a 10. Se for diferente, o resultado será False

#diferença
print(10 != 10) #O '!=' é o simbolo de diferença, ou seja, 10 é diferente de 10. Se for igual, o resultado será False

#maior que
print(10 > 10) #O '>' é o simbolo de maior que, ou seja, 10 é maior que 10. Se for menor, o resultado será False

#menor que
print(10 < 10) #O '<' é o simbolo de menor que, ou seja, 10 é menor que 10. Se for maior, o resultado será False

#maior ou igual
print(10 >= 10) #O '>=' é o simbolo de maior ou igual, ou seja, 10 é maior ou igual a 10. Se for menor, o resultado será False

#menor ou igual
print(10 <= 10) #O '<=' é o simbolo de menor ou igual, ou seja, 10 é menor ou igual a 10. Se for maior, o resultado será False

#comparadores em strings
print("hello" == "hello") #O '==' é o simbolo de igualdade, ou seja, hello é uma string igual a hello. Se for diferente, o resultado será False

print("hello" != "hello") #O '!=' é o simbolo de diferença, ou seja, hello é uma string diferente de hello. Se for igual, o resultado será False

print("hello" > "hello") #O '>' é o simbolo de maior que, ou seja, hello é uma string maior que hello. Se for menor, o resultado será False

print("hello" < "hello") #O '<' é o simbolo de menor que, ou seja, hello é uma string menor que hello. Se for maior, o resultado será False